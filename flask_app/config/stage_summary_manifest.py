def get_stage_summary_manifest() -> List[StageSummaryEntry]:
    """
    Accessor that returns a mutable copy of the manifest.

    The copy prevents accidental in-place mutation of the module-level
    definition while allowing callers to modify their working set.
    """

    return [entry.copy() for entry in STAGE_SUMMARY_MANIFEST]


