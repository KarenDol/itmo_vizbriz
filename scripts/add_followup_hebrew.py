def main():
    with open(PACKAGE_PATH, encoding='utf-8') as f:
        pkg = json.load(f)

    intro = pkg['metadata']['intro']
    intro.update(INTRO_HE)

    pkg['metadata']['section_title_he'] = SECTION_TITLE_HE

    for key, title_he in DISPLAY_GROUPS_HE.items():
        grp = pkg['metadata']['display_groups'].get(key)
        if grp:
            grp['title_he'] = title_he

    for q in pkg['questions']:
        qid = q['qid']
        if qid in QUESTION_TITLES_HE:
            q['title_he'] = QUESTION_TITLES_HE[qid]
        if qid in SUFFIX_HE:
            q['suffix_he'] = SUFFIX_HE[qid]
        labels = OPTION_LABELS_HE.get(qid)
        if labels and q.get('options'):
            for opt in q['options']:
                val = opt.get('value')
                if val in labels:
                    opt['label_he'] = labels[val]

    with open(PACKAGE_PATH, 'w', encoding='utf-8') as f:
        json.dump(pkg, f, ensure_ascii=False, indent=2)
        f.write('\n')

    print(f'Updated {PACKAGE_PATH}')


if __name__ == '__main__':
    main()
