"""
Stage Summary manifest configuration.

This file defines the ordered collection of stage descriptors used by the
stage summary API. Each entry should provide the metadata required to
evaluate completion status without embedding evaluation logic here.

Entries will be expanded in subsequent implementation phases. For now the
structure is scaffolded so that downstream components can import and iterate
over the manifest.
"""

from __future__ import annotations

from typing import List, TypedDict, Dict, Any

try:  # Python 3.11+
    from typing import NotRequired  # type: ignore[attr-defined]
except ImportError:  # Python <3.11
    try:
        from typing_extensions import NotRequired  # type: ignore
    except ImportError:
        class _CompatNotRequiredMeta(type):
            def __getitem__(self, item):
                return item

        class NotRequired(metaclass=_CompatNotRequiredMeta):  # type: ignore
            """Fallback no-op NotRequired for older Python versions."""

            pass


