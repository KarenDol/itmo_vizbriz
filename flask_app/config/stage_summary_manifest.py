    completion_args: Dict[str, Any]  # Arguments for the completion_type function
    guidance: str
    prerequisites: List[str]
    skip_if: NotRequired[List[str]]  # NEW: Stage keys that, if completed, allow this stage to be skipped
    optional: NotRequired[bool]  # NEW: Whether this stage is optional (can be skipped)
    validation_function: NotRequired[str]  # Optional: name of specific validation function
    llm_context_tag: NotRequired[str]
    feature_flag: NotRequired[str]


